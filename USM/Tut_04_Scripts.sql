DROP TABLE "BOOKS_IN_KL";
DROP TABLE "BOOKS_IN_PEN";
DROP TABLE "EMPLOYEE";
DROP TABLE "JOB";

ALTER SESSION SET NLS_DATE_FORMAT = 'DD-MON-YY';

CREATE TABLE  "BOOKS_IN_KL"(
   "ISBN" VARCHAR2(10) NOT NULL ENABLE, 
   "TITLE" VARCHAR2(30), 
   "PUB_DATE" DATE, 
   "CATEGORY" VARCHAR2(12), 
   "COST" NUMBER(5,2), 
   "RET_PRICE" NUMBER(5,2), 
   CONSTRAINT "BOOKS_IN_KL_ISBN_PK" PRIMARY KEY ("ISBN") ENABLE
);

CREATE TABLE  "BOOKS_IN_PEN"(
   "ISBN" VARCHAR2(10) NOT NULL ENABLE, 
   "TITLE" VARCHAR2(30), 
   "PUB_DATE" DATE, 
   "CATEGORY" VARCHAR2(12), 
   "COST" NUMBER(5,2), 
   "RET_PRICE" NUMBER(5,2), 
   CONSTRAINT "BOOKS_IN_PEN_ISBN_PK" PRIMARY KEY ("ISBN") ENABLE
);

CREATE TABLE  "JOB"(	
   "JOB_CODE" VARCHAR2(3), 
   "JOB_DESCRIPTION" VARCHAR2(25), 
   "JOB_CHG_HOUR" NUMBER, 
   CONSTRAINT "JOB_JOB_CODE_PK" PRIMARY KEY ("JOB_CODE") ENABLE
);

CREATE TABLE  "EMPLOYEE"(
   "EMP_NUM" VARCHAR2(3), 
   "EMP_LNAME" VARCHAR2(15), 
   "EMP_FNAME" VARCHAR2(15), 
   "EMP_INITIAL" VARCHAR2(1), 
   "EMP_HIREDATE" DATE, 
   "JOB_CODE" VARCHAR2(3), 
   CONSTRAINT "EMP_EMP_NUM_PK" PRIMARY KEY ("EMP_NUM") ENABLE, 
   CONSTRAINT "EMP_JOB_CODE_FK" FOREIGN KEY ("JOB_CODE") REFERENCES  "JOB" ("JOB_CODE") ON DELETE CASCADE ENABLE
);

